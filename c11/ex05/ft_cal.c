#include "do_op.h"

int	cal_plus(int a, int b)
{
	return (a + b);
}

int	cal_minus(int a, int b)
{
	return (a - b);
}

int	cal_times(int a, int b)
{
	return (a * b);
}

int	cal_div(int a, int b)
{
	return (a / b);
}

int	cal_mod(int a, int b)
{
	return (a % b);
}
